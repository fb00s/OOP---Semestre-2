    private void adNumOuVirgula(String numOuVirgula){
        if (reescreverNum){
            jLResultado.setText("0");
            reescreverNum = false;
        }
        String numAtualizado = jLResultado.getText();
        
        if (numAtualizado.equals("0") && !numOuVirgula.equals(",")){
            numAtualizado = numOuVirgula;
        } else {
            numAtualizado += numOuVirgula;
        }
        
        jLResultado.setText(numAtualizado);
        
        podeApagar = true;
    }
    
