    private String numeroFormatado(double numero){
        String strNum = Double.toString(numero);
        
        char ultimoCaracter = strNum.charAt(strNum.length() - 1);
        
        String strUltimoC = new String(new char[]{ultimoCaracter});
        
        String substring;
        
        if (strNum.contains(".")){
            while(strUltimoC.equals("0")){
                substring = strNum.substring(0, strNum.length() - 1);
                
                strNum = substring;
                
                ultimoCaracter = strNum.charAt(strNum.length() - 1);
                strUltimoC = new String(new char[]{ultimoCaracter});
            }
        }
        
        if (strUltimoC.equals(".")){
            substring = strNum.substring(0, strNum.length() - 1);
            strNum = substring;
        }
        
        return strNum.replace(".", ",");
    }
    
    private void escreverCampoAcima(){
        jLTextoAcima.setText(numeroFormatado(num1) + " " + operador);
        
    }
    
    private void setOperador(String charOp){
        operador = charOp;
        if (!reescreverNum){
            setNum1();
        }
        escreverCampoAcima();
        reescreverNum = true;
    }
    
