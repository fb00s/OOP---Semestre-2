    private void jBtnIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnIgualActionPerformed
        setNum2();
        resultado = 0;
        
        switch (operador){
            case "+":
                resultado = num1 + num2;
                break;
            case "-":
                resultado = num1 - num2;
                break;
            case "x":
                resultado = num1 * num2;
                break;
            case "÷":
                resultado = num1 / num2;
                break;
            default:
                break;
        }
        jLResultado.setText(numeroFormatado(resultado));
        jLTextoAcima.setText("");
        reescreverNum = true;
        podeApagar = false;
        setNum1();
    }//GEN-LAST:event_jBtnIgualActionPerformed

    private void jBtnApagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnApagarActionPerformed
        if (podeApagar){
          String strResultado = jLResultado.getText();
        
            String novaString = strResultado.substring(0, strResultado.length() - 1);
        
            if (novaString.equals("")){
                jLResultado.setText("0");
            } else {
                jLResultado.setText(novaString);
            }  
        }
    }//GEN-LAST:event_jBtnApagarActionPerformed

